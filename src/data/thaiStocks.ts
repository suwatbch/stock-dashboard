// ฟังก์ชันสำหรับดึงชื่อหุ้นไทย
export function getThaiStockName(
  symbol: string
): { name: string; sector: string } | null {
  // ลองหาจาก symbol โดยตรง
  if (thaiStockNames[symbol]) {
    return thaiStockNames[symbol];
  }

  // ลองหาจาก symbol โดยเพิ่ม .BK
  const withBK = `${symbol}.BK`;
  if (thaiStockNames[withBK]) {
    return thaiStockNames[withBK];
  }

  return null;
}

// ฟังก์ชันตรวจสอบว่าเป็นหุ้นไทยหรือไม่
export function isThaiStock(symbol: string): boolean {
  return symbol.endsWith('.BK') || symbol.endsWith('-R.BK');
}
